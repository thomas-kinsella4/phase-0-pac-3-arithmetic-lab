function add(x, y) {
    return x + y;
};

function subtract(x, y) {
    return x - y;
};

function multiply(x, y) {
    return x * y;
};

function divide(x, y) {
    return x / y;
};

function increment(n) {
    let num = n;
    num++;
    return num;
};

function decrement(n) {
    let num = n;
    num--;
    return num;
};

function makeInt(n) {
    return parseInt(n, 10);
};

function preserveDecimal(n) {
    return parseFloat(n);
};
